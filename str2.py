# string alignment
# center
text = "hello"
t = text.center(15,"-") # -----hello-----
print(t)


# ljust()
text = "hello"
t = text.ljust(15,"-") 
print(t)

# rjust()
text = "hello"
t = text.rjust(15,"-") 
print(t)

text = "42"
t = text.zfill(10)
print(t)


